// OPERATOR PERBANDINGAN
// digunakan untuk membandingkan dua buah value
// hasil dari operator perbandingan adalah boolean, 

// (==) -> sama dengan, setelah dilakukan konversi data, otomatis mengkonversi tipe data
// (===) -> identik , value, tipe data harus sama, jika tidak, akan false
// (!==) -> tidak identik

var_dump("10" == 10);
